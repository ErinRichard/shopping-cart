from math import floor as f, pi as p

def square_footage(l,w):
    """ 
    the square_footage(l,w) function calculates square footage 
    user needs to input length and width
    square footage = length * width
    """
    l = l
    w = w
    area = l * w
    return area




def circumf(r):
    """
    Function circumf(r) calculates circumference of a circle based on radius
    circumference = 2 * pi * radius
    User needs to input radius
    """
    c = r * p * 2
    return c

